#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	number;

	number = n;
	if (number < 0)
	{
		number *= -1;
		ft_putchar_fd('-', fd);
	}
	if (number > 9)
	{
		ft_putnbr_fd((number / 10), fd);
		ft_putchar_fd((number % 10 + '0'), fd);
	}
	else
		ft_putchar_fd((number + '0'), fd);
}